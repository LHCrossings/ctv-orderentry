_MARKET_MAP = {
    "central valley": "CVC",
    "san francisco":  "SFO",
    "los angeles":    "LAX",
    "seattle":        "SEA",
    "houston":        "HOU",
    "chicago":        "CMP",
    "washington":     "WDC",
    "new york":       "NYC",
}

_DAY_NORMALIZE = {
    "m-su":    "M-Su",
    "m-sun":   "M-Su",
    "m-f":     "M-F",
    "m-sa":    "M-Sa",
    "sa-su":   "Sa-Su",
    "sat-sun": "Sa-Su",
    "sa":      "Sa",
    "su":      "Su",
    "sun":     "Su",
    "sat":     "Sa",
}

# ─────────────────────────────────────────────────────────────────────────────
# DATACLASSES
# ─────────────────────────────────────────────────────────────────────────────

@dataclass(frozen=True)
class SierraLine:
    """A single Etere-ready airtime line from a Sierra Donor media plan."""
    language_block: str    # e.g. "Chinese", "Vietnamese", "Hmong"
    days: str              # normalized: "M-F", "M-Su", "Sa-Su"
    time_str: str          # raw range: "7p-12a", "11a-1p", "6p-8p"
    duration_seconds: int  # always 30 for current template
    start_date: str        # "05/01/2026" (MM/DD/YYYY)
    end_date: str          # "05/31/2026"
    spot_type: str         # "COM" or "BON"
    total_spots: int       # 0 = placeholder for multi-daypart split
    rate: float            # per-spot cost (0.0 for bonus)
    is_bonus: bool


@dataclass
class SierraOrder:
    """Complete parsed Sierra Donor media plan."""
    advertiser: str        # "Sierra Donor Services"
    market: str            # "CVC"
    date: str              # "04/24/2026"
    acct_rep: str          # "Marquice Wong"
    lines: List[SierraLine]


# ─────────────────────────────────────────────────────────────────────────────
# HELPERS
# ─────────────────────────────────────────────────────────────────────────────

def _normalize_market(text: str) -> str:
    text_lower = text.lower().strip()
    for key, code in _MARKET_MAP.items():
        if key in text_lower:
            return code
    return text.strip().upper()[:3]


def _normalize_date(raw: str) -> str:
    """Convert 'M/D/YYYY' → 'MM/DD/YYYY'."""
    raw = raw.strip()
    m = re.match(r'^(\d{1,2})/(\d{1,2})/(\d{4})$', raw)
    if m:
        mo, day, yr = m.groups()
        return f"{int(mo):02d}/{int(day):02d}/{yr}"
    return raw


def _parse_rate(raw: str) -> float:
    """Parse '$ 36.00' or '$ -' → float."""
    raw = raw.strip()
    if re.match(r'^\$?\s*-?\s*$', raw):
        return 0.0
    cleaned = re.sub(r'[\$\s,]', '', raw)
    try:
        return float(cleaned)
    except ValueError:
        return 0.0


def _parse_duration(raw: str) -> int:
    """Parse '30 seconds' → 30."""
    m = re.search(r'(\d+)', raw or "")
    return int(m.group(1)) if m else 30


def _normalize_days(raw: str) -> str:
    """Normalize day abbreviation to canonical Etere form."""
    return _DAY_NORMALIZE.get(raw.strip().lower(), raw.strip())


def _normalize_daypart_text(daypart: str) -> str:
    """
    Normalize raw daypart text from PDF:
    - Join embedded newlines (e.g. "8p-\n12a" → "8p-12a")
    - Collapse spaces around hyphens in day ranges (e.g. "Sat -Sun" → "Sat-Sun")
    """
    daypart = daypart.replace('\n', '')
    # "Sat -Sun" or "M -F" → "Sat-Sun" / "M-F"  (only between word chars)
    daypart = re.sub(r'(\w)\s+-\s*(\w)', r'\1-\2', daypart)
    return daypart.strip()


def _parse_daypart(daypart: str) -> Tuple[str, str]:
    """
    Parse a Day Part/Program field into a single (days, time_str) tuple.

    When multiple comma-separated dayparts appear on one row (e.g.
    "M-F (7p-12a), Sat-Sun 8p-12a"), they are collapsed into a single
    M-Su entry using the first segment's time — matching standard entry
    practice for combined day patterns.

    Handles:
      "M-F (7p-12a), Sat -Sun 8p-\\n12a"  → ("M-Su", "7p-12a")
      "M-Sun (11a-1p)"                     → ("M-Su", "11a-1p")
      "Sa-Su (6p-8p)"                      → ("Sa-Su", "6p-8p")
      "ROS BONUS"                          → ("M-Su", "6a-11:59p")
    """
    daypart = _normalize_daypart_text(daypart)

    if "BONUS" in daypart.upper():
        return ("M-Su", "6a-11:59p")

    parts = [p.strip() for p in daypart.split(',')]
    parsed = []

    for part in parts:
        # Pattern: "M-F (7p-12a)" — time in parens
        m = re.match(r'^([A-Za-z][\w\-]*)\s*\(([^)]+)\)\s*$', part)
        if m:
            parsed.append((_normalize_days(m.group(1)), m.group(2).strip()))
            continue

        # Pattern: "Sat-Sun 8p-12a" — time directly after days (no parens)
        m = re.match(r'^([A-Za-z][\w\-]*)\s+(\S.*)$', part, re.DOTALL)
        if m:
            parsed.append((_normalize_days(m.group(1)), m.group(2).strip()))
            continue

        parsed.append(("M-Su", "6a-11:59p"))

    if not parsed:
        return ("M-Su", "6a-11:59p")

    # Multiple segments → collapse to M-Su using the first segment's time
    if len(parsed) > 1:
        return ("M-Su", parsed[0][1])

    return parsed[0]


def _find_airtime_table(tables: list) -> Optional[list]:
    """Find the table that contains the 'Language Block' header row."""
    for table in tables:
        for row in table:
            if row and any(
                cell and "Language" in str(cell)
                for cell in row if cell
            ):
                return table
    return None


def _col_index(header_row: list, *candidates) -> Optional[int]:
    """Find column index matching any candidate substring (case-insensitive)."""
    for i, cell in enumerate(header_row):
        cell_str = str(cell or "").strip().lower().replace("\n", " ")
        for candidate in candidates:
            if candidate.lower() in cell_str:
                return i
    return None


