"""
Sierra Donor Services Media Plan Parser

Parses "Crossings TV Media Plan" PDFs generated by CTV AEs for Sierra Donor Services.

PDF Layout (text-based — pdfplumber extracts clean text):
  Header block:
    Crossings TV Media Plan  Option 2
    Date: M/D/YYYY  Station: Crossings TV
    Adertiser: <Advertiser>  Acct Rep: <AE Name>
    Client Email: ...  Email: ...
    Market <Market City>

  Airtime table columns:
    Language Block | Day Part/Program | Length | Start Date | End Date |
    Spot Type | Total Spot # | Spot Cost | Total Airtime Cost | Spot Value | Line Total Value

  Data rows:
    Chinese     | M-F (7p-12a), Sat-Sun 8p-12a | 30 seconds | 5/1/2026 | 5/31/2026 | COM | 40 | $36.00 | ...
    Vietnamese  | M-Sun (11a-1p)                | 30 seconds | 5/1/2026 | 5/31/2026 | COM | 32 | $30.00 | ...
    Hmong       | Sa-Su (6p-8p)                 | 30 seconds | 5/1/2026 | 5/31/2026 | COM | 20 | $30.00 | ...
    Chinese/... | ROS BONUS                     | 30 seconds | ...      | ...       | BON | 40 | $0     | ...

Key Business Rules:
  - Lines with comma-separated dayparts (e.g. "M-F (7p-12a), Sat-Sun 8p-12a") produce
    multiple SierraLine entries (one per daypart). Spot split is resolved at input time.
  - is_bonus: spot_type == "BON"
  - Market: "Central Valley" → "CVC"; parsed from header
  - Date format in PDF: M/D/YYYY → normalized to MM/DD/YYYY
  - Rate: "$ 36.00" → 36.0 (strip $ and spaces); "$ -" → 0.0
"""

