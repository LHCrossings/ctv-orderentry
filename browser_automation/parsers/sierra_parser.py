def parse_sierra_pdf(pdf_path: str) -> SierraOrder:
    """
    Parse a Crossings TV Media Plan PDF for Sierra Donor Services.

    Multi-daypart lines (comma-separated in the Day Part field) produce
    multiple SierraLine entries with total_spots=0 as a placeholder.
    The caller (gather_sierra_inputs) resolves the spot split before automation.

    Raises:
        ValueError: If the airtime table or critical columns cannot be found.
    """
    print(f"\n[SIERRA PARSER] Reading: {pdf_path}")

    with pdfplumber.open(pdf_path) as pdf:
        page = pdf.pages[0]
        full_text = page.extract_text() or ""
        tables = page.extract_tables() or []

    # ── Header fields ──────────────────────────────────────────────────────
    def _field(pattern: str) -> str:
        m = re.search(pattern, full_text)
        return m.group(1).strip() if m else ""

    # Template has the typo "Adertiser" (missing 'd') — handle both spellings
    advertiser = _field(r'[Aa]d[ev]?ertiser[:\s]+(.+?)(?:\s{2,}|Acct Rep|$)')
    acct_rep   = _field(r'Acct Rep[:\s]+(.+)')
    market_raw = _field(r'Market\s+(.+)')
    date_raw   = _field(r'Date[:\s]+([0-9/]+)')

    market = _normalize_market(market_raw) if market_raw else "CVC"
    date   = _normalize_date(date_raw)

    print(f"[SIERRA PARSER] Advertiser: {advertiser}")
    print(f"[SIERRA PARSER] Acct Rep:   {acct_rep}")
    print(f"[SIERRA PARSER] Market:     {market}")
    print(f"[SIERRA PARSER] Date:       {date}")

    # ── Airtime table ──────────────────────────────────────────────────────
    airtime_table = _find_airtime_table(tables)
    if not airtime_table:
        raise ValueError("[SIERRA PARSER] Could not find airtime table in PDF")

    header_row = None
    header_idx = 0
    for i, row in enumerate(airtime_table):
        if row and any(cell and "Language" in str(cell) for cell in row if cell):
            header_row = row
            header_idx = i
            break

    if header_row is None:
        raise ValueError("[SIERRA PARSER] Could not find Language Block header row")

    col_lang    = _col_index(header_row, "language")
    col_daypart = _col_index(header_row, "day part", "daypart", "program")
    col_length  = _col_index(header_row, "length")
    col_start   = _col_index(header_row, "start")
    col_end     = _col_index(header_row, "end")
    col_type    = _col_index(header_row, "spot type", "type")
    col_spots   = _col_index(header_row, "total spot", "spot #")
    col_cost    = _col_index(header_row, "spot cost")

    missing = [name for name, idx in [
        ("Language Block", col_lang),
        ("Day Part/Program", col_daypart),
        ("Start Date", col_start),
        ("End Date", col_end),
        ("Spot Type", col_type),
        ("Total Spot #", col_spots),
        ("Spot Cost", col_cost),
    ] if idx is None]
    if missing:
        raise ValueError(f"[SIERRA PARSER] Missing columns: {missing}")

    # ── Parse data rows ────────────────────────────────────────────────────
    sierra_lines: List[SierraLine] = []

    _SKIP = {"subtotal", "airtime summary", "airtime summary:"}

    for row in airtime_table[header_idx + 1:]:
        if not row:
            continue

        lang_raw = str(row[col_lang] or "").strip()
        if not lang_raw or lang_raw.lower() in _SKIP:
            continue

        daypart_raw = str(row[col_daypart] or "").strip()
        length_raw  = str(row[col_length]  or "30 seconds").strip() if col_length is not None else "30 seconds"
        start_raw   = str(row[col_start]   or "").strip()
        end_raw     = str(row[col_end]     or "").strip()
        type_raw    = str(row[col_type]    or "COM").strip().upper()
        spots_raw   = str(row[col_spots]   or "0").strip()
        cost_raw    = str(row[col_cost]    or "$0").strip()

        # Skip summary rows that leak into the language column
        if lang_raw.lower() in _SKIP:
            continue

        duration  = _parse_duration(length_raw)
        start     = _normalize_date(start_raw)
        end       = _normalize_date(end_raw)
        spot_type = type_raw
        is_bonus  = spot_type == "BON"
        rate      = 0.0 if is_bonus else _parse_rate(cost_raw)

        try:
            total_spots = int(re.sub(r'[^\d]', '', spots_raw))
        except (ValueError, TypeError):
            total_spots = 0

        days, time_str = _parse_daypart(daypart_raw)
        sierra_lines.append(SierraLine(
            language_block=lang_raw,
            days=days,
            time_str=time_str,
            duration_seconds=duration,
            start_date=start,
            end_date=end,
            spot_type=spot_type,
            total_spots=total_spots,
            rate=rate,
            is_bonus=is_bonus,
        ))

    if not sierra_lines:
        raise ValueError("[SIERRA PARSER] No data lines found in airtime table")

    print(f"[SIERRA PARSER] Parsed {len(sierra_lines)} Etere lines")
    for sl in sierra_lines:
        flag = "BONUS" if sl.is_bonus else f"${sl.rate:.2f}"
        placeholder = " [split TBD]" if sl.total_spots == 0 and not sl.is_bonus else ""
        print(f"  {sl.language_block:<30} {sl.days:<8} {sl.time_str:<15} "
              f"{sl.spot_type:<4} {sl.total_spots:>4} spots  {flag}{placeholder}")

    return SierraOrder(
        advertiser=advertiser,
        market=market,
        date=date,
        acct_rep=acct_rep,
        lines=sierra_lines,
    )


def parse_sierra(path: str) -> SierraOrder:
    """Dispatch to PDF or Excel parser based on file extension."""
    if Path(path).suffix.lower() in {".xlsx", ".xlsm"}:
        return parse_sierra_xlsx(path)
    return parse_sierra_pdf(path)


